# 5 ელემენტიანი tuple
my_tuple = (10, 20, 30, 40, 50)

# მეორე ელემენტის ამოღება
second_element = my_tuple[1]

# ბოლო ელემენტის ამოღება
last_element = my_tuple[-1]

# შუაში სამი ელემენტის ამოღება
middle_slice = my_tuple[1:4]

print("მეორე ელემენტი:", second_element)
print("ბოლო ელემენტი:", last_element)
print("შუა სამი ელემენტი:", middle_slice)
